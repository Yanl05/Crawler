# format 使用实例
print('{:.1f}'.format(4.234324525254))
print('{:.4f}'.format(4.1))
print('{:^14}'.format('陈某某'))
print('{:>14}'.format('陈某某'))
print('{:<14}'.format('陈某某'))
print('{:*<14}'.format('陈某某'))
print('{:&>14}'.format('陈某某'))
print('{:b}'.format(250))
print('{:o}'.format(250))
print('{:d}'.format(250))
print('{:x}'.format(250))
print('{:,}'.format(100000000))
print('{:,}'.format(235445.234235))
print('{}'.format(250))